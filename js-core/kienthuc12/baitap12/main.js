// tìm sô nguyên tố
//
//                                         ┌─────────────────────┐                 ┌────────────────┐
//                                         │                     │                 │                │
//                                         │     ta co n =0      │    n< 2         │                │
//    ┌─────────────────────┐   n > 2      │           x = true  ┼───────────────► │  n ko phai so  │
//    │                     │──────┬───────┤                     │                 │  nguyen  to    │
//    n % can bac 2 ===0    │      │       │                     │                 │                │
//    │   cua n      ->false│      │       └─────────────────────┘                 └────────────────┘
//    │                     │      │                  ▲
//    │                     │      │                  │
//    └─────────────────────┘      │                  │
//                                 ▼                  │
//                           ┌────────────────────────┴────────────────────────┐
//                           │                                                 │
//                           │   for (let i = 2; i <= Math.sqrt(n); i++) {     │
//                           │           if (n % i === 0) {                    │
//                           │               isPrime = false;                  │
//                           │                                                 │
//                           │                                                 │
//                           │                                                 │
//                           └─────────────────────────────────────────────────┘
//
//
//
//
let a = 3;
let isPrime = true;

//
if (a < 2) {
    isPrime = false;
} else {
    for (let i = 2; i <= Math.sqrt(a); i++) {
        if (a % i === 0) {
            isPrime = false;
            break;
        }
    }
}
if (isPrime) {
    console.log(a + " là số nguyên tố.");
} else {
    console.log(a + " không phải là số nguyên tố.");
}
