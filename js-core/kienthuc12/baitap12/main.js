//       no│
//         ▼
//
//    50 <x<= 100
//         │yes
//         ├────► amount = 1.678 * 50+1.734  * (x - 50)
//       no│
//   100<x<=200
//         │yes
//         ├────► amount = 1.678 * 50+1.734 * 50+2.014*(x-100)
//       no│
//         ▼
//
//  200< x <= 300
//          │es
//          ├───► amount =1.678 * 50 + 1.734 * 50 + 2.014 * 100 + 2.536 * (  x   - 200);
//       no │
//          ▼
//   300 <x <= 400
//          │yes
//          ├────► amount =1.678 * 50 + 1.734 * 50 + 2.014 * 100 + 2.536 * 100 + 2.834 * (  x   - 300);
//        no│
//
//        x > 400
//           │yes
//           └────► amount 1.678 * 50 + 1.734 * 50 + 2.014 * 100 + 2.536 * 100 + 2.834 * 100 + 2.927 * (   x  - 400);
