let cost = 0;
let s = 0;
// Tính toán chi phí
if (s <= 1) {
    cost = 15000;
} else if (s <= 5) {
    cost = 15000 + 13500 * (s - 1);
} else {
    cost = 15000 + 4 * 13500 + 11000 * (s - 5);
}

// Nếu s > 120 thì giảm 10%
if (s > 120) {
    cost = 0.9 * cost;
}

// In ra kết quả
console.log(cost);
