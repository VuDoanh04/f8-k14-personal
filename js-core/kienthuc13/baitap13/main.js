//          │     tao bien gan gia tri                    │
//          │                                             │
//          │    const arr   = [1, 2, 3, 2, 4];           │
//          │    const result = [];                       ┼──────────────────┐
//          │                                             │                  │
//          └─────────────────────────────────────────────┘                  │
//                                                                           │
//                                                                           │
//                                                                           │
//                                                                           │
//                                                                           │
//                                                                           │
//                                                                           │
//                                                                           ▼
//          ┌────────────────────────────────────┐          ┌──────────────── ──────────────────────────┐
//          │                                    │          │   for (let i = 0; i < arr.length; i++)    │
//          │   let j = 0; j < result.length; j++│          │                                           │
//          │                                    │          │    ┌──────────────────────────┐           │
//          │      arr[i] === result[j]          │◄─────────┼    │  let duplicate = false;  │           │
//          │                                    │          │    └──────────────────────────┘           │
//          │      duplicate = true;             │          │                                           │
//          │                  break;            │          │                                           │
//          └────────────────────────────────────┘          └─────────────────┬─────────────────────────┘
//                                                                            │
//                                                                            │
//                                                                            │
//                                                                            │
//                                                                            │
//                                                                            │
//                                                                            │
//                                                                            │
//                                                                            │
//                                                                            ▼
//                                                          ┌───────────────── ──────────────┐
//          ┌──────────────────────────────────┐            │                                │
//          │                                  │            │      duplicate === false       │
//          │  console.log(result);            │ ◄──────────┤      result.push(arr[i])       │
//          │                                  │            └────────────────────────────────┘
//          └──────────────────────────────────┘
//
//
//
const arr = [1, 2, 3, 2, 4];
const result = [];

for (let i = 0; i < arr.length; i++) {
    let duplicate = false;

    for (let j = 0; j < result.length; j++) {
        if (arr[i] === result[j]) {
            duplicate = true;
            break;
        }
    }

    if (duplicate === false) {
        result.push(arr[i]);
    }
}

console.log(result);
