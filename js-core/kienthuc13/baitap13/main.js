//      │                                     │
//      │  if (numbers[i] > max) {            │
//      │          max = numbers[i];          │
//      │          maxIndex = i;              │
//      │      }                              │
//      │      if (numbers[i] < min) {        │
//      │          min = numbers[i];          │
