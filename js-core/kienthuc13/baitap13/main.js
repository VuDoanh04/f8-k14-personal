//      │                                     │
//      └───────────────┬─────────────────────┘
//                      │
//                      │
//                      ▼
//      ┌─────────────────────────────────────────┐
//      │                                         │
//      │    In ra:                               │
//      │         "Min =", min, "at", minIndex
//      │         "Max =", max, "at", maxIndex
//      │                                         │
//      └─────────────────────────────────────────┘
let numbers = [5, 2, 8, 1, 6];
let max = numbers[0];
let min = numbers[0];
let maxIndex = 0;
let minIndex = 0;

for (let i = 1; i < numbers.length; i++) {
    if (numbers[i] > max) {
        max = numbers[i];
        maxIndex = i;
    }
    if (numbers[i] < min) {
        min = numbers[i];
        minIndex = i;
    }
}
console.log("Số lớn nhất là:", max, "ở vị trí:", maxIndex);
console.log("Số nhỏ nhất là:", min, "ở vị trí:", minIndex);
