//         ┌─────────▼────────────────┐                                    │
//         │                          │                                    │
//         │ a.sort((a, b) => a - b)  │                                    │
//         └─────────┬────────────────┘                                    ▼
//                   │                                       ┌──────────────────────────────────┐
//                   │                                       │                                  │
//         ┌─────────▼──────────────────────────────┐        │     let i = 0; i < a.length; i++ │
//         │                                        │        │                                  │
//         │     Nếu a - b < 0 → a đứng trước b.    │        └──────────────┬───────────────────┘
//         │                                        │                       │
//         │     Nếu a - b > 0 → b đứng trước a.    │                       │
//         │                                        │                       │
//         │     Nếu a - b === 0 → giữ nguyên thứ tự.                       │
//         │                                        │        ┌──────────────▼───────────────────────┐
//         └─────────▲──────────────────────────────┘        │   b <= a[i]       neu b=2            │
//                   │                                       │   location = i;    2 <= 1 Không đúng │
//                   │                                       │          break;    2 <= 2 Đúng       │
//                   │                                       │                     │                │
//          ┌────────▼─────────────────┐                     │                     ▼                │
//          │    console.log(a);       │                     │                    location = 2      │
//          │                          │                     └──────────────┬───────────────────────┘
//          └──────────────────────────┘                                    ▼
//                                                           ┌──────────────────────────────────────┐
//                                                           │     a.splice(location, 0, b);        │
//                                                           │                                      │
//                                                           └──────────────┬───────────────────────┘
//                                                                          ▼
//                                                            ┌─────────────────────────────────────┐
//                                                            │   console.log("bước 2:", a)         │
//                                                            │                                     │
//                                                            └─────────────────────────────────────┘
//
//
let a = [7, 5, 1, 3, 6];
let b = 2;

a.sort((a, b) => a - b);

console.log(a);
let location = a.length; // gán giá trị chèn vào cuối nếu không tìm được vị trí phù hợp.

for (let i = 0; i < a.length; i++) {
    if (b <= a[i]) {
        location = i;
        break;
    }
}

a.splice(location, 0, b);

console.log("bước 2:", a);
